			<h2>Driving and Parking</h2>
			
			<img src="../assets/park.jpg" alt="Parking" />
			
			<p>Unlike many other colleges, RIT doesn't restrict freshmen from driving. If living on-campus, a parking pass for your housing location is included with your housing contract. Visit the Parking and Transportation Services office in Grace Watson Hall to pick it up, and there will be information given on where you can park.</p>
			
			<p>If living off campus, head to the same location to purchase a commuter parking pass. Sadly, commuter parking is very limited around campus. If in a rush, the back areas of S-lot and T-lot are generally available regardless of the time, despite being rather far from campus. Taking a spot you can find and then walking is usually much faster than trying to hunt for a closer spot.</p>
			
			<p>While not recommended due to the potential for a parking ticket, parking in a University Commons parking lot is generally pretty easy to find a spot, and parking passess are only occasionally checked in these lots, meaning the risk is technically lower.</p>
			
			<p>One thing to note is that regardless of what parking pass you have, the campus has open parking after 5pm on weekdays and all the time on weekends. During these times, you can park in any spot that is not specifically marked as No Parking.</p>
			
