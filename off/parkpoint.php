			<p>Park Point is directly off of RIT's campus. In fact, it might be easy to mistake it as part of RIT campus, especially since the Barnes and Noble has the RIT logo prominiently featured on it. But Park Point is technically considered off-campus. In addition to being an off-campus housing option (albeit expensive one), Park Point also has a small shopping and dining plaza. The centerpiece is obviously the Barnes and Noble that also doubles as the campus bookstore, but the various dining options here are also solid.</p>
			
